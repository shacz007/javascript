// Math.ceil is a round up method too that rounds only upwards

let x = 2.6;
console.log(Math.ceil(x)); // 3

let y = 2.1;
console.log(Math.ceil(y)); //3



// observe both giving output as 3 & 3

// for negative

let a = -2.6;
console.log(Math.ceil(a)); // -2

let b = -2.1;
console.log(Math.ceil(b)); // -2